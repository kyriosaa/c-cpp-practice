#include <iostream>

int main() {
    // arithmetic operators = returns the result of a specific
    //                        arithmetic operation (+ - * /)

    int students = 20;
    
    students += 2;
    students -= 2;
    students *= 2;
    students /= 2;
    students % 20;

    students ++;
    students --;

    std::cout << students;

    return 0;
}